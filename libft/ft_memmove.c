#include <string.h>

void	*ft_memmove(void *dest, const void *src, size_t n)
{
	char		*s;
	char		*d;
	size_t		i;

	i = 0;
	d = (char *)dest;
	s = (char *)src;
	if (d > s)
	{
		while (n-- > 0)
			d[n] = s[n];
	}
	else
	{
		while (i < n)
		{
			d[i] = s[i];
			i++;
		}
	}
	return (dest);
}
