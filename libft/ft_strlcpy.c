#include <string.h>
#include "libft.h"

size_t	ft_strlcpy(char *dst, const char *src, size_t size)
{
	size_t	i;

	i = 0;
	if (!dst || !src)
		return (0);
	if (size > 0)
	{
		while (--size && src[i])
		{
			*dst = src[i];
			i++;
			dst++;
		}
		*dst = '\0';
	}
	while (src[i])
		i++;
	return (i);
}
